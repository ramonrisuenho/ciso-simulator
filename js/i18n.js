window.t = function(key, lang) {
    if (!lang) lang = 'pt';
    const keys = key.split('.');
    let val = window.i18n[lang];
    if (!val) return key;
    
    for (const k of keys) {
        if (!val) break;
        val = val[k];
    }
    return val || key;
};
